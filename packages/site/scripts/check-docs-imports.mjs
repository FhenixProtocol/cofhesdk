import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { spawnSync } from 'node:child_process';

import { globSync } from 'glob';
import ts from 'typescript';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..');

function findTwoslashTsBlocks(mdxContent) {
  // Matches fences like:
  // ```ts twoslash
  // ...
  // ```
  // Also handles extra labels e.g. ```ts twoslash [Ethers v6]
  const re = /```ts\s+twoslash[^\n]*\n([\s\S]*?)\n```/g;
  const blocks = [];
  let match;
  // eslint-disable-next-line no-cond-assign
  while ((match = re.exec(mdxContent))) {
    blocks.push(match[1]);
  }
  return blocks;
}

function extractImportStatements(tsCode) {
  const sourceFile = ts.createSourceFile('snippet.ts', tsCode, ts.ScriptTarget.Latest, true, ts.ScriptKind.TS);

  /** @type {string[]} */
  const imports = [];

  function visit(node) {
    if (ts.isImportDeclaration(node) || ts.isImportEqualsDeclaration(node)) {
      const text = tsCode.slice(node.getStart(sourceFile), node.getEnd());
      const trimmed = text.trim();
      if (trimmed) imports.push(trimmed.endsWith(';') ? trimmed : `${trimmed};`);
    }
    ts.forEachChild(node, visit);
  }

  visit(sourceFile);
  return imports;
}

function localTscBin() {
  const base = path.join(repoRoot, 'node_modules', '.bin', 'tsc');
  return process.platform === 'win32' ? `${base}.cmd` : base;
}

async function main() {
  const mdxPaths = globSync('pages/**/*.mdx', {
    cwd: repoRoot,
    absolute: true,
  });

  if (mdxPaths.length === 0) {
    console.log('No MDX files found under pages/.');
    process.exit(0);
  }

  const generatedDirRel = path.join('scripts', '.generated-docs-imports-check');
  const generatedDirAbs = path.join(repoRoot, generatedDirRel);

  await fs.rm(generatedDirAbs, { recursive: true, force: true });
  await fs.mkdir(generatedDirAbs, { recursive: true });

  /** @type {string[]} */
  const generatedFiles = [];
  let blockIndex = 0;

  for (const mdxPath of mdxPaths) {
    const content = await fs.readFile(mdxPath, 'utf8');
    const blocks = findTwoslashTsBlocks(content);

    for (const block of blocks) {
      // Twoslash blocks often include directives like `// @noErrors`.
      // Those are fine; we still want to validate the imports resolve.
      const imports = extractImportStatements(block);
      const uniqueImports = [...new Set(imports)].sort();
      if (uniqueImports.length === 0) continue;

      const outRel = path.join(generatedDirRel, `${String(blockIndex).padStart(4, '0')}.ts`);
      const outAbs = path.join(repoRoot, outRel);
      blockIndex += 1;

      const generated = `// This file is generated by scripts/check-docs-imports.mjs\n// Do not edit manually.\n\n${uniqueImports.join(
        '\n'
      )}\n\nexport {};\n`;

      await fs.writeFile(outAbs, generated, 'utf8');
      generatedFiles.push(outAbs);
    }
  }

  if (generatedFiles.length === 0) {
    await fs.rm(generatedDirAbs, { recursive: true, force: true });
    console.log('No import statements found in ts twoslash blocks.');
    process.exit(0);
  }

  const tsc = localTscBin();
  const tscArgs = [
    '--noEmit',
    '--pretty',
    'false',

    // Make this check about resolution/typing, not lint-style unused symbols.
    '--noUnusedLocals',
    'false',
    '--noUnusedParameters',
    'false',

    // Match docs site expectations.
    '--target',
    'ES2020',
    '--module',
    'ESNext',
    '--moduleResolution',
    'bundler',
    '--jsx',
    'react-jsx',
    '--lib',
    'ES2020,DOM,DOM.Iterable',
    '--skipLibCheck',

    ...generatedFiles,
  ];

  const result = spawnSync(tsc, tscArgs, {
    cwd: repoRoot,
    stdio: 'inherit',
  });

  // Always clean up the generated directory.
  await fs.rm(generatedDirAbs, { recursive: true, force: true });

  process.exit(result.status ?? 1);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
