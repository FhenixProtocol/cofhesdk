import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { spawnSync } from 'node:child_process';

import { globSync } from 'glob';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..');

function findTwoslashTsBlocks(mdxContent) {
  const re = /```ts\s+twoslash[^\n]*\n([\s\S]*?)\n```/g;
  const blocks = [];
  let match;
  // eslint-disable-next-line no-cond-assign
  while ((match = re.exec(mdxContent))) {
    const line = mdxContent.slice(0, match.index).split('\n').length;
    blocks.push({ code: match[1], line });
  }
  return blocks;
}

function containsIncludeDirective(code) {
  return code.includes('// [!include');
}

function containsNoErrorValidationDirective(code) {
  return code.includes('// @noErrorValidation');
}

/**
 * Strip twoslash and vocs directives so the block can be typechecked by tsc.
 * Keeps legitimate TS directives like `// @ts-expect-error`.
 */
function stripDirectives(code) {
  return code
    .split('\n')
    .filter((line) => {
      const trimmed = line.trim();
      if (trimmed === '// ---cut---') return false;
      if (trimmed === '// @noErrors') return false;
      if (trimmed.startsWith('// @errors:')) return false;
      if (trimmed.startsWith('// @filename:')) return false;
      if (trimmed.startsWith('// @noErrorValidation')) return false;
      return true;
    })
    .map((line) => {
      return line.replace(/\s*\/\/\s*\[!code\s[^\]]*\]\s*$/, '');
    })
    .join('\n');
}

function localTscBin() {
  const base = path.join(repoRoot, 'node_modules', '.bin', 'tsc');
  return process.platform === 'win32' ? `${base}.cmd` : base;
}

function runTsc({ cwd, project, count }) {
  const tsc = localTscBin();
  const tscArgs = ['--noEmit', '--pretty', '--project', project];

  console.log(`\nTypechecking ${count} snippet(s)...`);

  const result = spawnSync(tsc, tscArgs, { cwd, stdio: 'inherit' });
  return result.status ?? 1;
}

async function writeSnippet(outDir, relSource, line, cleaned) {
  const stem = relSource
    .replace(/^pages\//, '')
    .replace(/\.mdx$/, '')
    .replace(/\//g, '__');
  const outName = `${stem}__L${line}.ts`;
  const outAbs = path.join(outDir, outName);
  const header = `// Source: ${relSource}:${line}\n// Generated by scripts/typecheck-docs-snippets.mjs – do not edit.\n\n`;
  await fs.writeFile(outAbs, `${header}${cleaned}\n\nexport {};\n`, 'utf8');
  return outAbs;
}

async function main() {
  const mdxPaths = globSync('pages/**/*.mdx', {
    cwd: repoRoot,
    absolute: true,
  });

  if (mdxPaths.length === 0) {
    console.log('No MDX files found under pages/.');
    process.exit(0);
  }

  const hardhatTypecheckDir = path.join(
    repoRoot,
    'scripts',
    'hardhat-typecheck'
  );
  const generatedDir = path.join(hardhatTypecheckDir, '.generated');

  await fs.rm(generatedDir, { recursive: true, force: true });
  await fs.mkdir(generatedDir, { recursive: true });

  let count = 0;
  let skippedInclude = 0;
  let skippedNoErrorValidation = 0;

  for (const mdxPath of mdxPaths) {
    const content = await fs.readFile(mdxPath, 'utf8');
    const blocks = findTwoslashTsBlocks(content);
    const relPath = path.relative(repoRoot, mdxPath);

    for (const { code, line } of blocks) {
      if (containsIncludeDirective(code)) {
        skippedInclude++;
        continue;
      }

      if (containsNoErrorValidationDirective(code)) {
        skippedNoErrorValidation++;
        continue;
      }

      const cleaned = stripDirectives(code);
      if (cleaned.trim().length === 0) continue;

      await writeSnippet(generatedDir, relPath, line, cleaned);
      count++;
    }
  }

  const skippedTotal = skippedInclude + skippedNoErrorValidation;
  const skippedParts = [];
  if (skippedInclude > 0)
    skippedParts.push(`${skippedInclude} skipped due to [!include]`);
  if (skippedNoErrorValidation > 0)
    skippedParts.push(
      `${skippedNoErrorValidation} skipped due to @noErrorValidation`
    );
  const skippedLabel = skippedParts.length
    ? ` (${skippedTotal} skipped: ${skippedParts.join(', ')})`
    : '';
  console.log(`Found ${count} snippet(s)${skippedLabel}.`);

  if (count === 0) {
    await fs.rm(generatedDir, { recursive: true, force: true });
    console.log('No typecheck-able snippets found.');
    process.exit(0);
  }

  const exitCode = runTsc({
    cwd: hardhatTypecheckDir,
    project: path.join(hardhatTypecheckDir, 'tsconfig.json'),
    count,
  });

  await fs.rm(generatedDir, { recursive: true, force: true });

  if (exitCode === 0) {
    console.log('\nAll snippets passed typechecking.');
  }
  process.exit(exitCode);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});

/**
 * ## What this script does
 *
 * Extracts every `ts twoslash` fenced code block from the MDX documentation
 * pages, strips twoslash / vocs directives (`// ---cut---`, `// @noErrors`,
 * `// [!code ...]`, etc.), writes each block to a temporary `.ts` file, and
 * runs `tsc --noEmit` to verify they typecheck.
 *
 * All snippets are generated into `scripts/hardhat-typecheck/.generated/` and
 * typechecked in a single `tsc` pass using `hardhat-typecheck/tsconfig.json`.
 * This ensures Hardhat's module resolution and type augmentations are
 * available to every snippet regardless of which docs page it comes from.
 *
 * ## Why `hardhat-typecheck/` exists
 *
 * Hardhat snippets import `hre` from `"hardhat"` and rely on type
 * augmentations provided by plugins (`@cofhe/hardhat-plugin`,
 * `@nomicfoundation/hardhat-ethers`, etc.). These augmentations are only
 * activated when the plugins are imported in a `hardhat.config.ts` that lives
 * in the same resolution context as `tsc`. Because the site package is not a
 * Hardhat project, and pnpm's strict dependency isolation prevents Hardhat's
 * modules from resolving through the site's `node_modules`, we need a
 * dedicated workspace package that:
 *
 *  - Has `hardhat` and the relevant plugins as direct `devDependencies`.
 *  - Contains a minimal `hardhat.config.ts` that imports the plugins (which
 *    triggers their type augmentations on `HardhatRuntimeEnvironment`).
 *  - Has its own `tsconfig.json` with `node` module resolution (Hardhat
 *    requires CJS-compatible resolution, not bundler).
 *  - Is included in `pnpm-workspace.yaml` so dependencies are installed.
 *
 * Generated snippet files are written into `hardhat-typecheck/.generated/` and
 * cleaned up after the run.
 */
