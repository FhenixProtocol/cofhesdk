import { execSync } from 'child_process';
import * as fs from 'fs';
import { TASK_MANAGER_ADDRESS, MOCKS_ZK_VERIFIER_ADDRESS, MOCKS_THRESHOLD_NETWORK_ADDRESS } from '@cofhe/sdk';

const contracts: Array<{ name: string; fixedAddress?: string }> = [
  {
    name: 'MockTaskManager',
    fixedAddress: TASK_MANAGER_ADDRESS,
  },
  {
    name: 'MockZkVerifier',
    fixedAddress: MOCKS_ZK_VERIFIER_ADDRESS,
  },
  {
    name: 'MockThresholdNetwork',
    fixedAddress: MOCKS_THRESHOLD_NETWORK_ADDRESS,
  },
  {
    name: 'MockACL',
  },
];

function inspect(contract: string, field: string): any {
  const cmd = `forge inspect ${contract} ${field} --json`;
  const out = execSync(cmd, { encoding: 'utf8' }).trim();
  return JSON.parse(out);
}

const abiDir = 'abi';
fs.rmSync(abiDir, { recursive: true, force: true });
fs.mkdirSync(abiDir, { recursive: true });

function writeArtifactFile(name: string, artifact: Record<string, unknown>, abi: any): void {
  fs.writeFileSync(
    `src/${name}.ts`,
    `// This file is auto-generated by build-artifacts.ts
import { type MockArtifact } from './types';

export const ${name}Artifact = ${JSON.stringify(artifact, null, 2)} as const satisfies MockArtifact;`
  );

  fs.writeFileSync(`${abiDir}/${name}.json`, JSON.stringify(abi, null, 2));
}

function writeFixedAddressArtifact(name: string, fixedAddress: string, abi: any): void {
  const artifact = {
    contractName: name,
    isFixed: true,
    fixedAddress,
    abi,
  };

  writeArtifactFile(name, artifact, abi);
}

function writeVariableAddressArtifact(name: string, abi: any): void {
  const artifact = {
    contractName: name,
    isFixed: false,
    abi,
  };

  writeArtifactFile(name, artifact, abi);
}

for (const { name, fixedAddress } of contracts) {
  const abi = inspect(name, 'abi');

  if (fixedAddress) {
    writeFixedAddressArtifact(name, fixedAddress, abi);
  } else {
    writeVariableAddressArtifact(name, abi);
  }
}
